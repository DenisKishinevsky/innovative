        <p><strong>Nitrous 1-2-3</strong> is a unique strategy, inspired
          by enhanced proportions of liquids to solid de-icers used throughout 
          Europe and in several leading North American jurisdictions.  Liquids 
          used in proportions from 5% to 30% can dramatically reduce the use of 
          salt and drive greater economic efficiencies.  By combining the use of 
          premium quality liquids with lower eutectic temperatures and longer 
          lasting residual performance, these efficiencies are further enhanced. </p>
        <p>Fewer passes, faster cycle times and tremendous savings in material, labour, and fuel are just a few benefits of the <strong>Nitrous</strong> solution.  These benefits also combine to reduce our carbon footprint 
          and ensure economic as well as environmental sustainability.</p>
        <p><strong>Nitrous 1-2-3 Program:</strong></p>
        <ul class="body_list">
          <li><strong>Nitrous 1</strong> – Addition of 8-10 gallons (30–38L) of liquid de-icer to a dry ton of salt via on-board pre-wetting or by using treated salt</li>
          <li><strong>Nitrous 2</strong> – Addition of 8-10 gallons 
            (30-38 L) of liquid de-icer to one ton of salt that has already been 
            treated with 8-10 gallons of liquid de-icer</li>
          <li><strong>Nitrous 3</strong> – Addition of up to 50 
            gallons (190 L) of liquid de-icer to one ton of salt that has already 
            been treated with 8-10 gallons of liquid de-icer</li>
        </ul>
        <p>To fully understand the benefits of this program, please <a href="contact.php">contact us</a>.</p>
