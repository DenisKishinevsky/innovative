        <p>Our federally designated Design for the Environment (DfE) 
          MAGIC MINUS ZERO® 3070 BLEND  product is the ideal salt brine solution 
          to stretching your winter de-icing budget.</p>
        <p>Although one of the most cost effective salt brine solution 
          can be made by combining sodium chloride (NaCl) and water to form a 
          brine, its negative impact on the environment, the health risks 
          associated with increased airborne pollutants and high corrosive nature 
          far outweigh initial cost savings. </p>
        <p>MAGIC MINUS ZERO®  (salt brine enhancer) is our premium 
          performance catalyst when salt brine alone is not enough.  It is 
          available as a concentrate or in a 3070  blend and will significantly 
          increase your salt brine performance by:</p>
        <ul class="body_list">
          <li>Lowering the working temperature from -6°F (-21°C) to -24°F (-31°C)</li>
          <li>Lowering  the DLA (direct liquid application) rates</li>
          <li>Extending DLA time window (later in fall, earlier in spring)</li>
          <li>Increasing the coverage per load</li>
          <li>Contains a natural corrosion inhibitor</li>
          <li>Providing longer lasting residuals</li>
        </ul>
