        <h1>Green Commitment</h1>
        <h3>Products and services erroneously labelled 'environmentally 
          friendly' have resulted in nothing more than "green fatigue'. We believe
          that true environmental sustainability is linked to sound economic 
          stewardship and is dependant on the following:</h3>
        <ol>
          <li>A product must actually work.&nbsp; All the green seals,
            labels and good intentions  in the world will not compensate for a 
            product that fails to perform. </li>
          <li>A product if purported to be green must be  certified as
            such by an independent and impartial body.&nbsp; Certification programs
            maintained by  Underwriters Laboratories, Green Seal, Ecologo and US 
            EPA offer the best  indication of a product's claim to having been 
            designed on sound environmental  principles.</li>
          <li>A product or service must make financial  sense.&nbsp; 
            That doesn't necessarily mean it  must be the cheapest game in 
            town.&nbsp;  Frequently, premium products are offered at a higher price 
            but in turn deliver  performance efficiencies greater than their cheaper
            conventional  competitors.&nbsp; The total costs of  ownership or use 
            over the year in these cases is often far less costly.</li>
          <li>A product must be used in accordance with  
            manufacturer's guidelines.&nbsp; The old axiom  of "a little is good – 
            more must be better' certainly does not ring true.&nbsp; The over-use of
            any product exposes the  environment to a greater load and 
            stress.&nbsp;  Over-use is quite simply environmentally inefficient, 
            most likely  detrimental and a complete waste of money.</li>
        </ol>
