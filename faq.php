        <p><strong><a name="1"></a>What are the benefits of using Treated Salt compared to straight salt?</strong></p>
        <ul>
          <li>Lesser application rates</li>
          <li>Less “bounce &amp; scatter”</li>
          <li>Immediate brining (melting) action</li>
          <li>Long lasting “residual” left on road surface</li>
          <li>Less corrosion on spreaders &amp; equipment</li>
          <li>Free flowing stockpile / no clumping</li>
          <li>Environmental friendliness</li>
          <li>Works at lower temperatures</li>
        </ul>
        <p><strong><a name="2"></a>What are the typical cost factors?</strong></p>
        <ul>
          <li>Upfront cost is approximately 1/3 more, but overall infrastructure costs less</li>
        </ul>
        <p><strong><a name="3"></a>What is the time frame on deliveries?</strong></p>
        <ul>
          <li>Same as state/provincial contract requirements for dry material</li>
        </ul>
        <p><strong><a name="4"></a>What is the typical application rate for stockpile treatment?</strong></p>
        <ul>
          <li>Dependent on state/provincial contract &amp; product being applied between 5 to 8 gallons or 18 to 30 litres / ton of salt</li>
        </ul>
        <p><strong><a name="5"></a>What winter liquids should I consider?</strong></p>
        <ul>
          <li>You should consider our complete range of products. With the assistance of one of our Territory Managers you can determine the most appropriate liquid and/or solid to best suit your needs</li>
        </ul>
        <p><strong><a name="6"></a>What are your recommended application rates?</strong></p>
        <ul>
          <li>Application rates vary depending on the type of ice melter being used. Innovative’s environmentally safer products allow the user to put down less product per snow event, resulting in fewer chlorides being introduced into the environment while saving on fuel and labour costs</li>
        </ul>
        <p><strong><a name="7"></a>What is the lowest temperature at which your products will allow me to melt snow &amp; ice?</strong></p>
        <ul>
          <li>Innovative boasts a wide range of de-icers that vary in effective melting temperatures from -8°C to -49°C, but caution must be exercised to avoid refreezing of the diluted solution. We strongly advise detailed consultation with one of our Territory Managers</li>
        </ul>
        <p><strong><a name="8"></a>Does your product damage vegetation and concrete?</strong></p>
        <ul>
          <li>All de-icing products that contain chlorides can damage vegetation and concrete. With Innovative’s products the application rates are greatly reduced, resulting in less damaging chlorides being spread onto snow and ice covered surfaces. Also, damage from freeze and thaw cycles is a physical phenomenon and not a chemical one so using a product that prevents the frequency of this cycle would be recommended.</li>
        </ul>
        <p><strong><a name="9"></a>What’s in your product?</strong></p>
        <ul>
          <li>Innovative carries a wide range of products that consist of pure concentrations of liquid and solid magnesium and calcium chlorides to uniquely designed blended products to sodium chloride.</li>
        </ul>
        <p><strong><a name="10"></a>Is your product coloured?</strong></p>
        <ul>
          <li>Innovative has several products where color is inherent or added for greater visibility preventing over application</li>
        </ul>
        <p><strong><a name="11"></a>How does magnesium chloride control dust on a gravel surface?</strong></p>
        <ul>
          <li>Magnesium chloride is hygroscopic. It wants to attract and retain moisture. This aids in the compaction of gravel and retention of fine particles</li>
        </ul>
        <p><strong><a name="12"></a>Which is better for dust control &ndash; magnesium or calcium chloride?</strong></p>
        <ul>
          <li>Both are considered equal in terms of performance</li>
        </ul>
        <p><strong><a name="13"></a>I have a 500 gallon water tank. How much magnesium chloride do I need to make a 30% solution?</strong></p>
        <ul>
          <li>Please visit our website and use our convenient dilution calculator</li>
        </ul>
        <p><strong><a name="14"></a>How much does a 50lb bag of ProPatch cover?</strong></p>
        <ul>
          <li>5 to 6 square feet @ 1 inch depth. Anything deeper than 2 inches must be done in 2 inch lifts, making sure to leave a ¼ inch crown on top for compaction</li>
        </ul>
