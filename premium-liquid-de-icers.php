        <p>Liquid de-icing involves&nbsp;either&nbsp;a pro-active application or re-active placement of a liquid ice melting agent on the pavement surface prior to the accumulation of frozen precipitation, or to an already existing ice or hard pack snow surface. When applied in a stream pattern, performance enhancing liquids vertically penetrate the snowpack and break the bond at the road surface.Using liquid to &quot;pre-wet&quot; conventional solid de-icersalso reduces bounce and scatter, and environmental emissions while increasing performance and service levels.</p>
