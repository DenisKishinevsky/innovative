            14 stockpile and/or warehouse locations. ProPatch®&nbsp;is our permanent <a href="all-year/asphalt-repair-pro-patch">pothole patching</a> repair for utility cuts, joint repairs, outer edge repairs, surface 
            patches, broken pavement and large cracks. This fast working solution 
            allows the area to be opened for traffic immediately.</p>
          <p>In addition to pothole patching we offer our <strong>Asphalt Base Sealer</strong> which is a superior grade of asphalt cutback, ideally suited for both 
            residential and industrial paved surfaces. Special oils help rejuvenate 
            pavement surfaces eliminating the "checking" or "alligator cracking" 
            associated with other types of sealers.</p>
          <p>Specifically designed for the pavement contractor, <strong>Asphalt Base Sealer</strong> is not sold in retail stores. Innovative Surface Solutions' <strong>Asphalt Base Sealer</strong> is recommended for use on driveways, parking lots, runways or any type 
            of pavement surface where a fresh new look or rejuvenation is required.</p>
