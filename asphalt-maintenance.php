            quickly unless it is protected. This is further exacerbated as the 
            cracks filled with precipitation go through the freeze – thaw cycle with
            changing temperatures and resulting in further deterioration as the 
            asphalt breaks away and leaves some very unsightly holes on the surface.</h3>
          <p>Recognizing the importance of maintaining road safety, Innovative
            introduced their premium pothole patching material in 1986 with the 
            launch of <strong>ProPatch</strong>. This tried and true pothole patch 
