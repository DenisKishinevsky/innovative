    <h2><a name="LD"></a>Liquid De-Icers</h2>
    
    <p>Liquid de-icing involves <strong>either</strong> a pro-active 
application or re-active placement of a liquid ice melting agent on the 
pavement surface prior to the accumulation of frozen precipitation, or 
to an already existing ice or hard pack snow surface.  When applied in a
 stream pattern, performance enhancing liquids vertically penetrate the 
snowpack and break the bond at the road surface.</p>
    
