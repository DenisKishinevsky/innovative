    <h4><a name="BGD"></a>Bulk Granular Deicers</h4>
    
    <p>High performance solid de-icers including complex chloride blends
 and liquid enhanced pre-treated salts. These high-end products provide 
reduced application rates / waste, increased melting speed and capacity,
 while facilitating the use of common de-icers at lower temperatures.</p>
    
    <p>As a pre-wetting agent for salt and sand, chlorides enhanced with
 liquids reduce bounce and scatter, increase the speed at which the salt
 begins working, increase the melting performance of the salt, and 
facilitate the use of salt at lower temperatures.</p>
    
