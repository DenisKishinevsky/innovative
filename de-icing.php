    <h4><a name="ELD"></a>Enhanced Liquid De-Icers</h4>
    
    <p>The proactive application of enhanced solutions such as freeze 
point depressing liquids eliminates the bond between snow / ice and 
surface. Using liquid to "pre-wet" conventional solid de-icers reduces 
bounce and scatter, and environmental emissions while increasing 
performance and service levels.</p>
    
