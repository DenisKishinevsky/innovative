	<h2>The most readily available source of magnesium for crops</h2>
    <p>Plants drink, they don’t eat and magnesium is often unavailable to the plant as it is tied up by the soils negative charge. Innovative’s liquid NutriMag and AgMag are the most readily available sources of magnesium to crops.  At low soil temperatures (50-65 degrees) early in the season the solubility of other dry magnesium alternatives is very low.  The magnesium in NutriMag and AgMag are 3-5 times more available to the crop and is designed to be applied with liquid nitrogen.  </p>
    <h2>The NutriMag/UAN “Root Burst”</h2>
    <p>UAN applied with NutriMag causes a reaction which creates ammonium nitrogen stabilization and causes a cytokinin response or a “Root Burst”.<strong> </strong></p>
    <h2>Magnesium – Making the difference in Chlorophyll</h2>
    <p>Magnesium is vital to plant growth and health. It is the powerhouse behind photosynthesis in plants. Without magnesium, chlorophyll cannot capture sun energy that is needed for photosynthesis to occur.&nbsp; Magnesium in plants is located in the enzymes, in the heart of the chlorophyll molecule and give leaves their green color.</p>
    <h2>The Chloride connection</h2>
    <p>Chloride increases the uptake of Magnesium. I addition chloride plays an important role in many processes critical to plant health, animal, and human health. Plants require more than just water and sunlight to thrive. A delicate balance of macro and micronutrients in the soil are significant contributors to overall plant health. In plants chlorides help to prevent the accumulation of free amino acids which make the plants attractive to insect pests such as aphids. Chloride is necessary for gas exchange, photosynthesis, protection against diseases in plants and for efficient inter-cellular water transport management.&nbsp;</p>
    <h2>Increased quality and yields</h2>
    <p>Magnesium is also used by plants for the metabolism of carbohydrates and in the membrane stabilization. Many essential plant functions require adequate magnesium supplies, the most visible being its role in root formation, chlorophyll, and photosynthesis. Without these supplies, quality and yield decline. University studies across the country have shown time and again that Magnesium can improve yields and fruit quality in field crops.&nbsp;</p>
    <h2>Safe for the environment</h2>
    <p>Safe for the environment - our magnesium chloride is safe for the environment and AgMag is approved for organic production as crop fertilizer or soil amendment by OMRI (the Organic Materials Review Institute) in the United States and Canada.</p>
